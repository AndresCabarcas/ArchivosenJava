    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        if (m.Contador() == 0) {
            Archivo arc = new Archivo();
            try {
                arc.CargarArchivo("nombres.txt", m);
                for (int i = 0; i < m.Contador(); i++) {
                    if (m.arreglo.get(i).getGenero().equals("masculino")) {
                        cont++;
                        jLabel5.setText("Cantidad Hombres: " + cont);
                    } else {
                        if (m.arreglo.get(i).getGenero().equals("femenino")) {
                            segu++;
                            jLabel6.setText("Cantidad Mujeres: " + segu);
                        } else {
                            if (m.arreglo.get(i).getComidas() == 6 && m.arreglo.get(i).getGenero().equals("masculino")) {
                                terc++;
                                jLabel7.setText("Cantidad Diabeticos: " + terc);
                            }
                        }
                    }
                }
                actualizar();
            } catch (FileNotFoundException ex) {
                Logger.getLogger(Vista.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(Vista.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            if (m.Contador() >= 100) {
                JOptionPane.showMessageDialog(null, "Ya se han importado los datos: la pila no puede pasar su limite");
            }
        }

    }//GEN-LAST:event_jButton2ActionPerformed

