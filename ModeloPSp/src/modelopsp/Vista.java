    public void borrartabla() {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        while (modelo.getRowCount() > 0) {
            modelo.removeRow(0);
        }

    }

    public void actualizar() throws IOException {
// metodo para agregar elementos a la tabla
        borrartabla();
        if (m.Contador() > 0) {
            for (int i = 0; i < m.Contador(); i++) {//recorremos el Array
                //Asignar vista a la tabla
                String fila[] = new String[3];//definimos las 3 filas de la tabla
                fila[0] = m.pPersona(i).getNombre();//agregamos los elementos del array a la tabla por indice
                fila[1] = String.valueOf(m.pPersona(i).getCalorias());
                fila[2] = String.valueOf(m.pPersona(i).getComidas());
                n.addRow(fila);
            }
            jTable1.setModel(n);
        }
    }

