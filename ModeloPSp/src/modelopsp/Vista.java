    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed
        // TODO add your handling code here:
        //Asignamos a la variable Caloria la seleccion del ComboBox al igual que Comida(cantidad de comidas al dia)
        int Caloria = Integer.parseInt(Calorias.getSelectedItem().toString());
        int Comida = Integer.parseInt(Comidas.getSelectedItem().toString());
        //condicionamos y comparamos la seleccion si se agrega un hombre nuestro contador sumara 1 y lo mostrara en el TextLabel
        if (Genero.getSelectedItem().toString().equals("masculino")) {
            cont++;
            jLabel5.setText("Cantidad Hombres: " + cont);
        } else {
            if (Genero.getSelectedItem().toString().equals("femenino")) {
                segu++;
                jLabel6.setText("Cantidad Mujeres: " + segu);
            }
        }
        if (Comida == 6 && Genero.getSelectedItem().toString().equals("masculino")) {
            terc++;
            jLabel7.setText("Cantidad diabeticos: " + terc);
        }
        //Aca agregamos un nuevo objeto a nuestro ArrayList, llamando al constructor de la clase y tomando los valores del Jframe
        m.Agregar(new Persona(Nombre.getText(), Genero.getSelectedItem().toString(), Caloria, Comida));
        //encerramos el metodo actualizar en un try catch por que se pueden presentar errores al guardar en nuestra tabla
        try {
            actualizar();
        } catch (IOException ex) {
            Logger.getLogger(Vista.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jToggleButton1ActionPerformed

